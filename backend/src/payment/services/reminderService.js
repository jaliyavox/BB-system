/**
 * FILE: reminderService.js
 * PURPOSE: Handle all reminder-related business logic
 * DESCRIPTION: Create automatic reminders for payment cycles (pre-payment 5 days before, overdue after start date)
 */

const mongoose = require('mongoose');
const Notification = require('../../models/Notification');
const PaymentCycle = require('../../models/PaymentCycle');
const User = require('../../models/User');

/**
 * Create a pre-payment reminder (5 days before payment cycle starts)
 * @param {String} studentId - Student object ID
 * @param {Object} paymentCycle - PaymentCycle document
 * @returns {Object} Created Notification
 */
exports.createPrePaymentReminder = async (studentId, paymentCycle) => {
  try {
    // Don't create duplicate pre-payment reminders
    const existingReminder = await Notification.findOne({
      userId: studentId,
      type: 'payment_pre_payment',
      paymentCycleId: paymentCycle._id,
    });

    if (existingReminder) {
      console.log('⏭️  Pre-payment reminder already exists for cycle:', paymentCycle.cycleNumber);
      return null;
    }

    const startDate = new Date(paymentCycle.startDate);
    const formattedStartDate = startDate.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
    });

    const reminder = new Notification({
      userId: new mongoose.Types.ObjectId(studentId),
      type: 'payment_pre_payment',
      title: '🔔 Payment Window Opening Soon',
      message: `Your payment for cycle ${paymentCycle.cycleNumber} will open on ${formattedStartDate}. Prepare your payment slip now!`,
      reminderType: 'pre_payment',
      autoGenerated: true,
      paymentCycleId: paymentCycle._id,
      cycleNumber: paymentCycle.cycleNumber,
      isRead: false,
      createdAt: new Date(),
    });

    const saved = await reminder.save();
    console.log('✅ Pre-payment reminder created for student:', studentId, 'Cycle:', paymentCycle.cycleNumber);
    return saved;
  } catch (error) {
    console.error('❌ Error creating pre-payment reminder:', error.message);
    throw error;
  }
};

